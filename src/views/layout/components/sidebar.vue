<template>
    <div class="layout-sider">
        <slot name="logo"></slot>
        <Menu ref="sideMenu" :active-name="$route.name" :open-names="openNames" :theme="menuTheme" width="auto" @on-select="changeRoute" @on-open-change="changeOpenNames">
            <template v-for="item in showMenuList">
                <MenuItem v-if="!item.hidden && item.children.length===1" :name="item.children[0].name" :key="'menuitem' + item.name">
                <Icon :type="item.meta.icon" :size="iconSize" :key="'menuicon' + item.name"></Icon>
                <span class="layout-text" :key="'title' + item.name">{{ item.children[0].meta.title }}</span>
                </MenuItem>

                <Submenu v-if="!item.hidden && item.children.length > 1" :name="item.name" :key="item.name">
                    <template slot="title">
                        <Icon :type="item.meta.icon" :size="iconSize"></Icon>
                        <span class="layout-text">{{ item.meta.title }}</span>
                    </template>
                    <template v-for="child in item.children">
                        <MenuItem :name="child.name" :key="'menuitem' + child.name">
                        <Icon :type="child.meta.icon" :size="iconSize" :key="'icon' + child.name"></Icon>
                        <span class="layout-text" :key="'title' + child.name">{{ child.meta.title }}</span>
                        </MenuItem>
                    </template>
                </Submenu>
            </template>
        </Menu>
    </div>
</template>

<script>
export default {
  name: "sidebar",
  props: {
    menuList: Array,
    iconSize: {
      type: Number,
      default: 20
    },
    menuTheme: {
      type: String,
      default: "dark"
    },
    openNames: {
      type: Array
    }
  },
  components: {},
  data() {
    return {}
  },
  computed: {
        showMenuList(){
          let showMenuList = []
           this.menuList.forEach(item => {
              if (!item.hidden) {
                if (item.children && item.children.length) {
                  let children = item.children.filter(element => {
                    return !element.hidden
                  })
                  item.children = children
                }
                showMenuList.push(item)
              }
            })
            return showMenuList
        }

  },
  methods: {
    changeRoute(name) {
       this.$emit('on-select', name);
    },
    changeOpenNames(names) {
      this.$emit('on-open-change', names);

    }
  },
  watch: {},
  created() {},
  updated() {
    this.$nextTick(() => {
      if (this.$refs.sideMenu) {
        this.$refs.sideMenu.updateOpened();
      }
    });
  }
};
</script>

